function buyTokens() {
    var ethAmount = $('#ethAmount').val()

    // amount of Ether to send on the transaction
    let txObject = {
        'value' : web3.toWei(ethAmount, 'ether')
    }

    // call function buyTokens() from contract
    property.buyTokens.sendTransaction(txObject, function(error, result){
        if(error) {
            console.log(error)
        } else {
            $('#txHash').val(result)
        }
    })
}

function formatNumber(number) {
    return number.toString().replace(/(\d)(?=(\d{3})+(?!\d))/g, '$1,')
}