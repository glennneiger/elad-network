function buyTokens() {
    var ethAmount = $('#ethAmount').val()
    var text = 'Amount of ETH to send: ' + ethAmount

    // tokensLeft()

    let txObject = {
        'value' : web3.toWei(ethAmount, 'ether'),
        'input' : '0x44616e69656c'
    }

    // call function buyTokens() from contract
    property.buyTokens.sendTransaction(txObject, function(error, result){
        if(error) {
            console.log(error)
        } else {
            $('#txHash').val(result)
        }
    })
}

